class TriggerCreate(BaseModel):
    name: str
    kind: str
    config: dict = {}
    enabled: bool = True


class TriggerUpdate(BaseModel):
    name: str | None = None
    kind: str | None = None
    config: dict | None = None
    enabled: bool | None = None


@bp.get("/swarms/<swarm_id>/triggers")
def list_triggers(swarm_id: str):
    with get_session() as session:
        rows = session.execute(
            select(Trigger).where(Trigger.swarm_id == swarm_id).order_by(Trigger.name)
        ).scalars().all()
        return jsonify([t.to_dict() for t in rows])


@bp.post("/swarms/<swarm_id>/triggers")
@require_permission("can_manage_triggers")
def create_trigger(swarm_id: str):
    try:
        body = TriggerCreate.model_validate(request.get_json(force=True) or {})
    except Exception as exc:
        return jsonify({"error": {"code": "validation_error", "message": str(exc)}}), 400

    if body.kind not in _VALID_KINDS:
        return jsonify({
            "error": {
                "code": "invalid_kind",
                "message": f"kind must be one of: {', '.join(sorted(_VALID_KINDS))}",
            }
        }), 400

    with get_session() as session:
        if not session.get(Swarm, swarm_id):
            return jsonify({"error": {"code": "not_found", "message": "Swarm not found"}}), 404

    config = dict(body.config or {})

    # Friendly mapping for GUI: accept "cron" as an alias for "schedule".
    if body.kind == KIND_HEARTBEAT:
        if "cron" in config and "schedule" not in config:
            config["schedule"] = config.pop("cron")
        # Auto-generate a default tick script when none is supplied.
        config = _ensure_heartbeat_script(
            swarm_id, body.name, config, current_app.config["DATA_DIR"]
        )

    with get_session() as session:
        trigger = Trigger(
            swarm_id=swarm_id,
            name=body.name,
            kind=body.kind,
            config_json=json.dumps(config),
            enabled=body.enabled,
        )
        session.add(trigger)
        session.commit()
        session.refresh(trigger)
        result = trigger.to_dict()
        new_id = trigger.id

    tdir = _triggers_dir(swarm_id, current_app.config["DATA_DIR"])
    if tdir:
        with get_session() as session:
            t = session.get(Trigger, new_id)
            if t:
                _write_trigger_yaml(tdir, t)

    if body.kind == KIND_HEARTBEAT and body.enabled:
        _register_heartbeat_now(new_id)

    return jsonify(result), 201


@bp.put("/triggers/<trigger_id>")
@require_permission("can_manage_triggers")
def update_trigger(trigger_id: str):
    try:
        body = TriggerUpdate.model_validate(request.get_json(force=True) or {})
    except Exception as exc:
        return jsonify({"error": {"code": "validation_error", "message": str(exc)}}), 400

    with get_session() as session:
        trigger = session.get(Trigger, trigger_id)
        if not trigger:
            return jsonify({"error": {"code": "not_found", "message": "Trigger not found"}}), 404

        old_name   = trigger.name
        swarm_id   = trigger.swarm_id
        old_tdir   = _triggers_dir(swarm_id, current_app.config["DATA_DIR"])

        if body.name is not None:
            trigger.name = body.name
        if body.kind is not None:
            if body.kind not in _VALID_KINDS:
                return jsonify({
                    "error": {"code": "invalid_kind", "message": f"kind must be one of: {', '.join(sorted(_VALID_KINDS))}"}
                }), 400
            trigger.kind = body.kind
        if body.config is not None:
            trigger.config_json = json.dumps(body.config)
        if body.enabled is not None:
            trigger.enabled = body.enabled

        session.commit()
        session.refresh(trigger)
        result = trigger.to_dict()
        is_heartbeat = trigger.kind == KIND_HEARTBEAT
        is_enabled   = trigger.enabled

    if old_tdir:
        if body.name is not None and body.name != old_name:
            _delete_trigger_yaml(old_tdir, old_name)
        _write_trigger_yaml(old_tdir, trigger)

    # Reconcile the scheduler with the new state.
    if is_heartbeat:
        if is_enabled:
            _register_heartbeat_now(trigger_id)
        else:
            _deregister_heartbeat(trigger_id)

    return jsonify(result)


@bp.delete("/triggers/<trigger_id>")
@require_permission("can_manage_triggers")
def delete_trigger(trigger_id: str):
    with get_session() as session:
        trigger = session.get(Trigger, trigger_id)
        if not trigger:
            return jsonify({"error": {"code": "not_found", "message": "Trigger not found"}}), 404
        kind     = trigger.kind
        name     = trigger.name
        swarm_id = trigger.swarm_id
        tdir     = _triggers_dir(swarm_id, current_app.config["DATA_DIR"])
        session.delete(trigger)
        session.commit()

    if tdir:
        _delete_trigger_yaml(tdir, name)

    if kind == KIND_HEARTBEAT:
        _deregister_heartbeat(trigger_id)

    return "", 204


# ── Listener webhook ──────────────────────────────────────────────────────────

@bp.post("/triggers/listener/<path:suffix>")
@require_permission("can_start_run")
def listener_webhook(suffix: str):
    """Receive an inbound webhook for a listener trigger.

    The trigger is identified by the ``endpoint`` field in its config matching
    the URL suffix.  If the config declares a ``secret``, the request must
    carry ``Authorization: Bearer <secret>``.

    If the config includes a ``filter`` (a JSON-schema object), the request
    body is validated against it before the event is accepted.
    """
    # Find the matching listener trigger by endpoint suffix
    with get_session() as session:
        candidates = session.execute(
            select(Trigger).where(
                Trigger.kind == KIND_LISTENER,
                Trigger.enabled.is_(True),
            )
        ).scalars().all()

        trigger = None
        config: dict = {}
        for t in candidates:
            cfg = json.loads(t.config_json or "{}")
            if cfg.get("endpoint") == suffix:
                trigger = t
                config = cfg
                break

    if trigger is None:
        return jsonify({"error": {"code": "not_found", "message": "No listener trigger for this endpoint"}}), 404

    # Validate shared secret when configured
    expected_secret = config.get("secret")
    if expected_secret:
        auth = request.headers.get("Authorization", "")
        token = auth.removeprefix("Bearer ").strip()
        if token != expected_secret:
            return jsonify({"error": {"code": "unauthorized", "message": "Invalid or missing secret"}}), 401

    payload = request.get_json(force=True, silent=True) or {}

    # Deduplication: if payload carries an event_id we've seen in the last 24h, drop it
    event_id_key = payload.get("event_id")
    if event_id_key is not None:
        dedup_key = f"{trigger.id}:{event_id_key}"
        if _listener_dedup.is_duplicate(dedup_key):
            logger.debug("Listener %s: duplicate event_id %r — dropped", trigger.id, event_id_key)
            return jsonify({"deduplicated": True, "event_id": str(event_id_key)}), 202

    # Optional JSON-schema filter
    schema_filter = config.get("filter")
    if schema_filter:
        try:
            import jsonschema  # noqa: PLC0415
            jsonschema.validate(instance=payload, schema=schema_filter)
        except jsonschema.ValidationError as exc:
            return jsonify({"error": {"code": "filter_mismatch", "message": str(exc.message)}}), 422
        except Exception as exc:
            logger.warning("Listener filter error for trigger %s: %s", trigger.id, exc)

    # Persist and publish
    payload.setdefault("type", "listener")
    # Per-trigger target_agent overrides the swarm's entry_point (Phase 6.1).
    if config.get("target_agent"):
        payload["_target_agent"] = config["target_agent"]

    with get_session() as session:
        event = Event(
            swarm_id=trigger.swarm_id,
            trigger_id=trigger.id,
            source="listener",
            payload_json=json.dumps(payload),
        )
        session.add(event)
        session.commit()
        session.refresh(event)

    current_app.event_bus.publish(event)
    return jsonify({"event_id": event.id}), 202


