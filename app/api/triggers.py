# ── Listener deduplication ────────────────────────────────────────────────────

class _DedupeCache:
    """Thread-safe in-memory LRU dedup store with TTL.

    Keyed by an arbitrary string (typically payload.event_id).
    Returns True if the key was seen within the TTL window (duplicate).
    Evicts entries older than ttl_seconds on each write; also evicts the
    oldest entry when the cache is at capacity.
    """

    def __init__(self, ttl_seconds: int = 86400, max_size: int = 10_000) -> None:
        self._cache: OrderedDict[str, float] = OrderedDict()
        self._lock = threading.Lock()
        self._ttl = ttl_seconds
        self._max_size = max_size

    def is_duplicate(self, key: str) -> bool:
        """Return True if *key* is a recent duplicate; record it if not."""
        now = time.monotonic()
        with self._lock:
            if key in self._cache:
                return True
            # Evict expired entries (oldest first)
            cutoff = now - self._ttl
            while self._cache:
                oldest_key, oldest_ts = next(iter(self._cache.items()))
                if oldest_ts < cutoff:
                    self._cache.popitem(last=False)
                else:
                    break
            # Evict oldest entry if at capacity
            if len(self._cache) >= self._max_size:
                self._cache.popitem(last=False)
            self._cache[key] = now
            return False


_listener_dedup = _DedupeCache(ttl_seconds=86400, max_size=10_000)

_VALID_KINDS = {KIND_HEARTBEAT, KIND_LISTENER, KIND_INVOCATION}


# ── Heartbeat helpers ─────────────────────────────────────────────────────────

_DEFAULT_HEARTBEAT_SCRIPT = '''\
"""Default heartbeat script generated by the GUI.

Emits one ``heartbeat`` event per tick. The watermark advances to the
current UTC timestamp so the runtime can resume cleanly across restarts.
"""
import json
import sys
from datetime import datetime, timezone

now = datetime.now(timezone.utc).isoformat()
print(json.dumps({
    "watermark": now,
    "events": [
        {"type": "heartbeat", "payload": {"tick": now}}
    ],
}))
'''


def _ensure_heartbeat_script(swarm_id: str, name: str, config: dict, data_dir: str) -> dict:
    """If a heartbeat config is missing ``script``, generate a default one on disk
    and patch the config to point at it. Returns the (possibly mutated) config.
    """
    if config.get("script"):
        return config

    with get_session() as session:
        swarm = session.get(Swarm, swarm_id)
        if not swarm:
            return config
        workspace = session.get(Workspace, swarm.workspace_id)
        if not workspace:
            return config
        ws_name = workspace.name
        sw_name = swarm.name

    triggers_dir = os.path.join(data_dir, "workspaces", ws_name, "swarms", sw_name, "triggers")
    os.makedirs(triggers_dir, exist_ok=True)
    script_filename = f"{name}.py"
    script_path = os.path.join(triggers_dir, script_filename)
    if not os.path.isfile(script_path):
        with open(script_path, "w") as f:
            f.write(_DEFAULT_HEARTBEAT_SCRIPT)
        os.chmod(script_path, 0o755)
    config["script"] = script_filename
    return config


def _register_heartbeat_now(trigger_id: str) -> None:
    """Pick up a freshly-saved heartbeat trigger and register it with the
    running scheduler so it starts firing without a process restart.
    """
    from app.core.heartbeat import register_heartbeat_trigger

    data_dir = current_app.config["DATA_DIR"]
    with get_session() as session:
        trigger = session.get(Trigger, trigger_id)
        if not trigger:
            return
        # Detach a usable copy so we can drop the session before scheduling.
        session.expunge(trigger)
    try:
        register_heartbeat_trigger(current_app._get_current_object(), current_app.event_bus, data_dir, trigger)
    except Exception:
        logger.exception("Failed to register heartbeat %s", trigger_id)


def _deregister_heartbeat(trigger_id: str) -> None:
    try:
        from app.scheduler import remove_heartbeat
        remove_heartbeat(trigger_id)
    except Exception:
        logger.exception("Failed to remove heartbeat job %s", trigger_id)


# ── Trigger file helpers ──────────────────────────────────────────────────────

def _triggers_dir(swarm_id: str, data_dir: str) -> str | None:
    """Return the swarm's triggers/ directory path, creating it if needed."""
    with get_session() as session:
        swarm = session.get(Swarm, swarm_id)
        if not swarm:
            return None
        workspace = session.get(Workspace, swarm.workspace_id)
        if not workspace:
            return None
        path = os.path.join(
            data_dir, "workspaces", workspace.name, "swarms", swarm.name, "triggers"
        )
    os.makedirs(path, exist_ok=True)
    return path


def _write_trigger_yaml(triggers_dir: str, trigger: Trigger) -> None:
    config = json.loads(trigger.config_json or "{}")
    data = {"kind": trigger.kind, "enabled": trigger.enabled, "config": config}
    tmp = os.path.join(triggers_dir, f"{trigger.name}.yaml.tmp")
    dst = os.path.join(triggers_dir, f"{trigger.name}.yaml")
    with open(tmp, "w") as f:
        yaml.dump(data, f, allow_unicode=True, sort_keys=False, default_flow_style=False)
    os.replace(tmp, dst)


def _delete_trigger_yaml(triggers_dir: str, name: str) -> None:
    path = os.path.join(triggers_dir, f"{name}.yaml")
    try:
        os.remove(path)
    except FileNotFoundError:
        pass


