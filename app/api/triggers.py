@bp.post("/triggers/invocations/<trigger_id>")
@require_permission("can_start_run")
def invoke_trigger(trigger_id: str):
    """Manually invoke a swarm via an invocation trigger.

    The request body is the event payload.  An ``invoked_by`` field may be
    included to identify the caller; it defaults to ``"anonymous"``.

    The invocation config may include a ``schema`` field (JSON Schema) to
    validate the submitted payload before the event is created.
    """
    with get_session() as session:
        trigger = session.get(Trigger, trigger_id)
        if not trigger:
            return jsonify({"error": {"code": "not_found", "message": "Trigger not found"}}), 404
        if trigger.kind != KIND_INVOCATION:
            return jsonify({"error": {"code": "wrong_kind", "message": "Trigger is not an invocation"}}), 400
        if not trigger.enabled:
            return jsonify({"error": {"code": "disabled", "message": "Trigger is disabled"}}), 409

        config = json.loads(trigger.config_json or "{}")
        swarm_id = trigger.swarm_id
        t_id = trigger.id

    body_payload = request.get_json(force=True, silent=True) or {}
    invoked_by = body_payload.pop("invoked_by", "anonymous")

    payload_schema = config.get("payload_schema")
    if payload_schema:
        # New schema-based payload assembly:
        # 1. Start with any extra free-form fields from the caller.
        payload = dict(body_payload)
        # 2. Merge fixed fields (always override caller input).
        for field in payload_schema:
            key  = field.get("key")
            mode = field.get("mode", "input")
            if not key:
                continue
            if mode == "fixed":
                payload[key] = field.get("value")
            elif key not in payload:
                if field.get("required"):
                    return jsonify({"error": {
                        "code": "missing_field",
                        "message": f"Required field '{key}' not provided",
                    }}), 422
                if "default" in field:
                    payload[key] = field["default"]
        # 3. Auto-parse text fields that contain valid JSON — the GUI
        #    collects structured data as raw JSON text in textareas.
        for key, val in list(payload.items()):
            if isinstance(val, str):
                stripped = val.strip()
                if stripped and stripped[0] in ("{", "["):
                    try:
                        payload[key] = json.loads(stripped)
                    except (json.JSONDecodeError, ValueError):
                        pass
    elif not body_payload and config.get("default_payload"):
        # Legacy: fall back to stored default_payload when body is empty.
        payload = dict(config["default_payload"])
    else:
        payload = body_payload

    # Optional JSON-Schema validation (legacy config.schema field)
    schema = config.get("schema")
    if schema:
        try:
            import jsonschema  # noqa: PLC0415
            jsonschema.validate(instance=payload, schema=schema)
        except jsonschema.ValidationError as exc:
            return jsonify({"error": {"code": "validation_error", "message": exc.message}}), 422

    payload.setdefault("type", "invocation")
    payload["invoked_by"] = invoked_by
    if config.get("target_agent"):
        payload["_target_agent"] = config["target_agent"]

    with get_session() as session:
        event = Event(
            swarm_id=swarm_id,
            trigger_id=t_id,
            source="invocation",
            payload_json=json.dumps(payload),
        )
        session.add(event)
        session.commit()
        session.refresh(event)

    current_app.event_bus.publish(event)
    return jsonify({"event_id": event.id}), 202
